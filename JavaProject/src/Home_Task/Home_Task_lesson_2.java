package Home_Task;

public class Home_Task_lesson_2 {
    public static void main(String[] args) {
        byte myByte = -128;
        byte myByte2 = 127;

        short myShort = -32768;
        short myShort2 = 32767;

        int myInt = -2147483648;
        int myInt2 = 2147483647;

        long myLong = -9223372l;
        long myLong2 = 922337203l;

        System.out.println("myByte = " + myByte);
        System.out.println("myByte2 = " + myByte2);

        System.out.println("myShort = " + myShort);
        System.out.println("myShortTwo = " + myShort2);

        System.out.println("myInt = " + myInt);
        System.out.println("myIntTwo = " + myInt2);

        System.out.println("myLong = " + myLong);
        System.out.println("myLongTwo = " + myLong2);

        float myFloat = 1.2f;
        float myFloat2 = 123.1f;

        System.out.println("myFloat = " + myFloat);
        System.out.println("myFloatTwo = " + myFloat2);

        double myDouble = 21.1234;
        double myDouble2 = 456.12312412;

        System.out.println("myDouble = " + myDouble);
        System.out.println("myDoubleTwo = " + myDouble2);

        boolean isTrue = true;
        boolean isFalse = false;

        System.out.println("isTrue = " + isTrue);
        System.out.println("isFalse = " + isFalse);

        char myChar = 0;
        System.out.println("myChar = " + myChar);

        char myChar2;
        myChar2 = 6553;
        System.out.println("myChar2 = " + myChar2);

        char myChar3, myChar4, myChar5;
        myChar3 = 'a';
        myChar4 = '\u0054';
        myChar5 = '5';
        System.out.println("myChar3 = " + myChar3);
        System.out.println("myChar4 = " + myChar4);
        System.out.println("myChar5 = " + myChar5);
    }
}
