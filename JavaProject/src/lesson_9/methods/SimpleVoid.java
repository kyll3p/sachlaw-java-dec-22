package lesson_9.methods;

public class SimpleVoid {
    public static void main(String[] args) {
        sayHelloToClass();
    }

    public static void sayHelloToClass() {
        System.out.println("Hello, Alex!");
        System.out.println("Hello, Nastya!");
        System.out.println("Hello, Barak Obama!");
        System.out.println("Hello, Bibi Netanyahu!");
    }
}
